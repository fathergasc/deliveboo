    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\User  $user
     * @param  \App\Order  $order
     * @return mixed
     */
    public function view(User $user, Order $order)
    {
        //if the function returns true, the user is authorized

        $userRestaurant = Restaurant::all()->where('user_id', $user->id)->first();
        $products = Product::all()->where('restaurant_id', $userRestaurant->id);

        $orders = [];
        //array containing only unique IDs of orders
        $ordersIds = [];
        foreach($products as $product){
            foreach($product->orders as $order){
                if(!in_array($order->id, $ordersIds)) {
                    $ordersIds[] = $order->id;
                    $orders[] = $order;
                }
            }
        }

        if(in_array($order->id, $ordersIds )) {
            return true;
        }

    }

